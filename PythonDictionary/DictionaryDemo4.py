#4字典方法
#1、clear方法
#clear方法清除字典中所有的项，这个是原地操作，类似(list.sort)所以无返回值(或者是返回None)
d={}
d['name']='CY'
d['age']=23
print(d)
returned_value=d.clear()
print(d)
print(returned_value)
#使用clear的意义是真正清空原来的字典
#情况一,将x关联到一个空字典可以清空x，但是不会影响到y，y还关联原来的字典。
x={}
y=x
x['key']='value'
print(y)
x={}
print(x)
print(y)
#情况二,使用clear方法可以清空原来的字典
x1={}
y1=x1
x1["key"]="value"
print(y1)
x1.clear()
print(x1)
print(y1)
